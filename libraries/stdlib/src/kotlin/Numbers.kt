package kotlin

/**
 * Returns {@code true} if the specified number is a
 * Not-a-Number (NaN) value, {@code false} otherwise.
 */
public fun Double.isNaN(): Boolean = this != this

/**
 * Returns {@code true} if the specified number is a
 * Not-a-Number (NaN) value, {@code false} otherwise.
 */
public fun Float.isNaN(): Boolean = this != this